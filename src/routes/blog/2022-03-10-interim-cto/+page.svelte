			<h1 class="text-3xl font-bold mb-4">
				Why Startups Should Use an Interim CTO in the Early Stages
			</h1>
			<p class="mb-4 md:text-base">10 Mar 2022</p>
			<p class="mb-4 md:text-base">Written By Michael Distel</p>
			<h2 class="text-2xl font-bold mt-8 mb-4">What is a CTO in a modern tech startup?</h2>
			<p class="mb-4 text-xl leading-9">
				If you think being a Chief Technology Officer (CTO) of a small startup = being a CTO of a
				large company with hundreds of employees, multiple teams and a wide range of technical
				requirements, it’s not really the case.
			</p>
			<p class="mb-4 md:text-xl leading-9">
				Here’s what my years of experience being in a small startup taught me. Working in a startup
				means mostly focusing on a single goal, building tech and aligning it with the changing
				landscape of its business goals. Having flexibility is key as compared to having a perfect
				system.
			</p>
			<p class="md:text-xl">Small startup CTOs will normally be required to</p>
			<ul class="list-disc list-inside md:text-xl leading-9">
				<li>Translate business goals into actionable goals for the tech team.</li>
				<li>Manage budgeting and costing to control burn rates and report to stakeholders.</li>
				<li>
					Pitch decks and speak to investors about the technical requirements to get the funding
					needed to build the tech.
				</li>
				<li>
					Possess deep technical knowledge to ensure the company is building the tech in the right
					direction.
				</li>
				<li>Build technical teams to get the job done.</li>
				<li>
					Handle project management - including estimating and meeting company business objectives.
				</li>
				<li>
					Depending on how small the startup is, the CTO may be the Chief Architect, Technical Lead,
					Developer, Test Engineer, Product Manager.
				</li>
			</ul>
			<h2 class="text-2xl font-bold mt-8 leading-9">
				Challenges of hiring a full-time CTO or Head of Engineering in the early stages of a tech
				startup.
			</h2>
			<p class="mb-4 md:text-xl leading-9">
				There is always a risk with hiring any staff but with key positions like CTO or Head of
				Engineering, any form of “hiring mistake” can have an extensive negative impact on the
				startup’s chance of success. I’ve gone and compiled a list of challenges faced by startup
				founders while trying to hire a full-time CTO. Here’s a quick summary of what I’ve found:
			</p>
			<ul class="list-disc list-inside md:text-xl leading-9">
				<li>
					Time to hire can drag on for months, which eventually snowball effects and delays the
					start of development
				</li>
				<li>
					Cost, it is very costly for an early-stage startup and asking for one to believe in the
					company at the early stage and take equity can be a hard talk.
				</li>
				<li>
					As it is hard to hire, it is now normal for most employment contracts to have a 2 to 3
					month notice period. It's very painful to remove them if you work out later they are not
					right for the job.
				</li>
				<li>
					It is hard for non-technical founders to know “what it takes to be a good CTO or Head of
					Engineering?”
				</li>
			</ul>

			<h2 class="text-2xl font-bold mt-8">How is an interim different from a full-time CTO?</h2>
			<ul class="list-disc list-inside md:text-xl">
				<li>
					Normally an interim CTO acts and behaviours like a full-time CTO but there are some things
					to take note of.
				</li>
				<li>
					They have less time to build culture, while that can be used to start or change a culture,
					it will be hard for them to be the “core” of that culture.
				</li>
				<li>
					It does not make sense for them to do coding or day-to-day work themselves. The money
					saved from using them only part-time should be directed to boots on the ground.
				</li>
			</ul>
			<h2 class="text-2xl font-bold mt-8">
				What are some of the positives of hiring an interim CTO?
			</h2>
			<ul class="list-disc list-inside md:text-xl">
				<li>
					They can start very quickly with no need to wait for a notice period from the previous
					jobs.
				</li>
				<li>
					They are less costly to hire, from recruitment, no 20% fee and take fewer resources to
					find. They are normally looking for you and making sure they are easy to find.
				</li>
				<li>
					You can remove them very quickly if they are not performing and not up to the task at
					close to no cost.
				</li>
				<li>
					Gives you the ability to control your budgets as they are flexible when and how they work.
				</li>
			</ul>

			<h2 class="text-2xl font-bold mt-8">Different situations to use an interim CTO</h2>
			<h3 class="font-bold mt-8 md:text-xl">Pre-investment</h3>
			<p class="mb-4 md:text-xl">
				At this very early stage of a startup, money is very limited to a point where even an
				interim CTO may be too expensive. It is normal for these CTOs to only offer advice and
				guidance for the price of a coffee with a focus on how to communicate with the investors.
				It's during this stage where founders need help with understanding what investors are
				looking for and how to cut out most of the technical jargon.
			</p>
			<h3 class="font-bold mt-8 md:text-xl">Seed / Angel</h3>
			<p class="mb-4 md:text-xl">
				Once a little money is in the bank, it is time to start building a prototype or working on a
				proof of concept. During this time, most founders will hire their first developers either in
				house or by outsourcing. An interim CTO can really help with setting up and advising the
				founding members about future requirements. With that said, money is still limited and the
				interim will play more of an advisory role. Assist with the cost analytics, pitch decks and
				other support to help to propel the startup in the direction towards the next stage of
				funding.
			</p>
			<h3 class="font-bold mt-8 md:text-xl">Pre-series A</h3>
			<p class="mb-4 md:text-xl">
				Pre-series A is where an interim CTO can be the greatest asset to the company. Money is
				still tight, sales are still not high enough and the burn rate is high. Having the ability
				to be flexible with the budget and moving quickly to minimize the impact of a high burn rate
				is definitely of top priority. Hiring an expensive full-time CTO mightnot make sense but it
				is also at this point in time the fundamentals like product roadmaps, building your first
				development team and setting a clear vision of what the product will be requires some
				experience.
			</p>
			<h3 class="font-bold mt-8 md:text-xl">Series A</h3>
			<p class="mb-4 md:text-xl">
				During series A it's time for growth and expansion, with millions in the bank and hopefully
				a long runway. It's during this time that an interim can be used to assist in hiring and
				working with the founders to plan for the future tech leadership team. It's easier to use
				equity to get the cost down and the tech team is now big enough to have a full-time leader.
				It’s also time for the interim CTO to step aside and leave the future to someone else.
			</p>
			<h3 class="font-bold mt-8 md:text-xl">Series B and beyond</h3>
			<p class="mb-4 md:text-xl">
				Once the company has reached this stage, then they already have a proven business model and
				hiring an interim CTO is less about managing the budget and more about handling unique
				situations. While it is sad when it happens, there are times when the directors need to
				quickly remove the troublesome CTO without enough time to hire a new one. Lastly, there are
				times when the company needs to make unpopular changes to the tech team and it makes sense
				to use an outside person to do it.
			</p>
			<h2 class="text-2xl font-bold mt-8 md:text-xl">Conclusion</h2>
			<p class="mb-4 md:text-xl">
				Ideally from day one, there should be a perfect leadership team, with great people in all
				the key roles, sales, finance, marketing, engineering. But let's be honest, startups can't
				afford and don't have all the time in the world to find them right from the start. Hiring an
				interim CTO can prove to be a great stepping stone to success for startups.
			</p>

