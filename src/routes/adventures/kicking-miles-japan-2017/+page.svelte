	<h3 class="text-lg sm:text-xl font-semibold mb-2">What I Learned</h3>
	<p class="mb-4 md:text-xl sm:text-base">
		Forty-five kilometres daily turned out to be sustainable. Eighty would have broken us. This
		taught me something about the difference between intensity and consistency that I still apply to
		other projects.
	</p>

	<div class="mb-6">
		<h4 class="text-md font-semibold mb-2 text-gray-300">Notes to Self:</h4>
		<ul class="list-disc list-inside space-y-2 md:text-xl sm:text-base ml-4">
			<li>
				<strong>Sustainable beats heroic:</strong> Daily progress compounds; sprints lead to burnout
			</li>
			<li>
				<strong>Plans are starting points:</strong> Flexibility saved us more than rigid schedules
			</li>
			<li>
				<strong>Team coordination:</strong> Four people, different strengths, shared direction
			</li>
			<li>
				<strong>Slow reveals more:</strong> Smaller cities showed us Japan beyond the guidebooks
			</li>
			<li><strong>Know your limits:</strong> When to push harder, when to rest, when to adjust</li>
		</ul>
	</div>

	<p class="mb-6 md:text-xl sm:text-base">
		I still use this approach for long-term projects—break them into daily progress rather than
		attempting heroic weekend pushes. Forty days of manageable effort beats four days of
		unsustainable intensity.
	</p>

